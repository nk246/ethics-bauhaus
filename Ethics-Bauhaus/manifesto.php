            <h2>The Manifesto</h2>
            <p>The Bauhaus Module is an interdisciplinary space where students from all faculties at the Bauhaus University Weimar collaborate and create their seminar. In the summer of 2023 the class named "Ethics @ Bauhaus" focused on ethical consequences of advancements in information science and their impact on the future of society, democracy and the freedom of an individual. This manifesto resumes the classes' outcome and serves as a guideline for future technological development.</p>


<ol>
  <li>Military research in cyberweapons and its commercialization has to be stopped. Using language technology as a weapon is especially dangerous as language is abstract. This leads to levels of destruction which are unimaginable.</li>
  <li>The research in new technologies has to stay in the hands of programmers from the open source community, scientists, NGOs or institutions like libraries which serve the common good of society. Development in the free market has to shift from a few big companies to numerous small or medium sized ones to break monopolies and create a diverse landscape of tools not used as weapons.</li>
  <li>Prioritize users' health when designing digital tools and encourage responsible usage.</li>
  <li>A modern society needs clear explanations of algorithms' decision-making to eradicate biases and promote equality through human-rights protection.</li>
  <li>When developing new technologies, it is important to consider their impact on a sustainable relation between humans and nature.</li>
  <li>Ensure free and liberal access to technologies in educational context.</li>
  <li>The application of technology has to be made clear during the development to prevent misusage afterwards.</li>
  <li>Take responsibility when executing AI systems and be accountable for the outcome.</li>
  <li>An awareness of using the new technologies as tools for improving a state of being is necessary to assist the progress of human evolution rather than hinder critical and independent thinking.</li>
  <li>Regularly evaluate and reassess the ethical implications of digital tools, engaging in transparent dialogues with users, experts, and policymakers to adapt and improve practices accordingly.</li>
</ol>
